const Mongoose = require('mongoose');

const todos_collection = 'Todos';

const Todo = Mongoose.model(todos_collection, {
  text: {
    type: String,
    required: true,
    minlength: 1,
    trim: true
  },
  completed: {
    type: Boolean,
    default: false
  },
  completedAt: {
    type: Number,
    default: null
  }
});

module.exports = {Todo}